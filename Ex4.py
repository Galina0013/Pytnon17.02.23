ve = float(input("Введите выручку фирмы: "))
i = float(input("Введите издержки фирмы: "))
if ve > i:
	p = ve - i
	r = round(p / ve, 1)
	print("Финансовый результат - прибыль. Рентабельность выручки: ", r)
n = float(input("Введите численность сотрудников: "))
print("Прибыль фирмы в расчете на одного сотрудника: ", p / n)
